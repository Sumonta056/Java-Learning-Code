package basic_java;
// * Wrapper Class(help access primitive date type)
//    *   byte	Byte
//    *   short	Short
//    *   int	Integer
//    *   long	Long
//    *   float	Float
//    *   double	Double
//    *   boolean	Boolean
//    *   char	Character

// * here int is datatype
// * Integer is object data type
// * cz java Object Orientated Programming language

public class j16_wrapperClass {
    public static void main(String[] args) {
        Integer myInt = 5;
        Double myDouble = 5.99;
        Character myChar = 'A';
        System.out.println(myInt);
        System.out.println(myDouble);
        System.out.println(myChar);
    }

}
