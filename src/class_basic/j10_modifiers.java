package class_basic;

public class j10_modifiers {
    // * 1 - Access Modifiers
        // ! For Classes
        // * public - class is accessible by any other class
        // * default - class is only accessible by classes in the same package

        // ! attributes, methods and constructors
        // * public - accessible for all classes
        // * private - only accessible within the declared class
        // * default - only accessible in the same package
        // * protected - accessible in the same package and subclasses

    // * 2 - Non-Access Modifiers
        // * final - cannot be overridden/modified/inherited
        // * static - belongs to the class, rather than an object
        // * abstract - The class cannot be used to create objects

    // * cannot change the value of PI
    final double PI = 3.14;



}
